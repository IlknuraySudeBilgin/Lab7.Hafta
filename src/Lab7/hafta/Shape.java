package Lab7.hafta;

public class Shape {

	protected String ad;
	protected String alan;
	
	
	public Shape(String ad, String alan) {
		super();
		this.ad = ad;
		this.alan = alan;
	}


	public String getAd() {
		return ad;
	}


	public void setAd(String ad) {
		this.ad = ad;
	}


	public String getAlan() {
		return alan;
	}


	public void setAlan(String alan) {
		this.alan = alan;
	}
	
	
	
	
	
	
	
	
	
}
